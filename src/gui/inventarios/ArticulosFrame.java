    private void inciarArticuloCaptura() {

        if (articuloCaptura == null) {

            articuloCaptura = new gui.inventarios.ArticuloCaptura();
        }
        if (!articuloCaptura.isVisible()) {

            articuloCaptura = new gui.inventarios.ArticuloCaptura();

            articuloCaptura.cargar();

            escritorio.remove(articuloCaptura);
            escritorio.add(articuloCaptura);
            articuloCaptura.centrado(escritorio.getSize());
            articuloCaptura.setModal(true);

        } else {

            articuloCaptura.setVisible(false);
        }
    }

