    private void keyPressed(java.awt.event.KeyEvent evt) {

        switch(evt.getKeyCode()){
            case KeyEvent.VK_ENTER:
                acceder();
                break;
            case KeyEvent.VK_ESCAPE:
                salir();
                break;
        }
    }
    
